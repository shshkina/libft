#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int q;
	int z;

	q = 0;
	z = 0;
	while (s1[q] != '\0')
		q++;
	while (s2[z] != '\0')
	{
		s1[q] = s2[z];
		q++;
		z++;
	}
	s1[q] = '\0';
	return (s1);
}
